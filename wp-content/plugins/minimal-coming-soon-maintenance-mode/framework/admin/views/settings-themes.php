function csmm_themes_sort($item1, $item2) {
  if (strtotime($item1['last_edit']) == strtotime($item2['last_edit'])) {
    return 0;
  }
  return strtotime($item1['last_edit']) < strtotime($item2['last_edit']) ? 1 : -1;
}
